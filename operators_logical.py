a = True
b = False

print("a AND,&& b is ",(a and b))	# it return true when both a and b are true, otherwise false
print("a OR,|| b is ",(a or b))		# it return true when one of the value is true, otherwise false
print("NOT,! a is ",(not a))		# when a is true it return false
print("NOT,! b is ",(not b))		# when b is false it return true
