a = 10
b = 5

a += b		# a = a + b
			# a = 10 + 5		
print(a)	# a = 15

a -= b		# a = a - b
print(a)	# a = 10

a *= b		# a = a * b
print(a)	# a = 50

a /= b		# a = a / b
print(a)	# a = 10.0

a %= b		# a = a % b
print(a)	# a = 0


a = 2
