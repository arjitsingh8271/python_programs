# Set items are unordered, unchangeable, and do not allow duplicate values.

st = {1, "mango", 2.3, "A"}
print(st)                   # Set is unordered  {2.3, 'mango', 1, 'A'}
print(type(st))             #<class 'set'>
print(len(st))              #4

