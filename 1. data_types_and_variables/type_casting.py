print(type(a))
a = int(a)		# now it is numeric literal
print(type(a))
print(" ")

b = 34	
print(type(b))
b = str(b)
print(type(b))
print(" ")

c = 12.3
print(type(c))
c = int(c)
print(type(c))
print(" ")

d = 14
print(type(d))
d = float(d)
print(type(d))
print(" ")


