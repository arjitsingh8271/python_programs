'''
Range Data Type in Python:
We can create a range of values by using the range() function. 
The range data type represents a sequence of numbers. 
The range data type is immutable means we cannot modify it once it is created. 
Range data type values can be printed by using for loop.
'''

a=range(5)
print(a)
for x in a:
    print(x)

'''
OUTPUT:
range(0, 5)
0
1
2
3
4
'''
