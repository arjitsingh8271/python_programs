for i in range(5):	# range(stop)its print 0 to 4
	print(i)
print()


for i in range(2, 5):	# range(start, stop) its print 2 to 4
	print(i)
print()


for i in range(1, 10, 2):	# range(start, stop, step-size)
	print(i)				# its skip one item after 1, then 3 and so on and print 1 3 5 7 9



